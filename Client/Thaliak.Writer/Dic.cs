        static Dictionary<int, Dictionary<int, string>> allDic = new Dictionary<int, Dictionary<int, string>>();

        internal static void SaveDictionaries(int dataVersion, string path, string gamePath, string lang)
        {
            var version = new Dictionary<int, string>();
            version[dataVersion] = "CurrentVersion";

            var isCN = lang.ToLower() == "cn";
            var mapDic = new Dictionary<int, string>();
            var zoneDic = new Dictionary<int, string>();
            var worldDic = new Dictionary<int, string>();
            var itemDic = new Dictionary<int, string>();

            #region ACT

            var md = ModuleDefinition.ReadModule(path);
            var mr = md.Resources;

            foreach (var res in mr)
            {
                if(res.Name.ToLower() == $"FFXIV_ACT_Plugin.Resources.TerritoryList_EN.txt".ToLower())
                {
                    using (var sr = new StreamReader((res as EmbeddedResource).GetResourceStream()))
                    {
                        var str = sr.ReadToEnd();
                        foreach (var s in str.Split('\n'))
                        {
                            var q = s.Split('|');
                            mapDic[int.Parse(q[0])] = q[1];
                        }
                    }
                }
                if (res.Name.ToLower() == $"FFXIV_ACT_Plugin.Resources.WorldList.txt".ToLower())
                {
                    using (var sr = new StreamReader((res as EmbeddedResource).GetResourceStream()))
                    {
                        var str = sr.ReadToEnd();
                        foreach (var s in str.Split('\n'))
                        {
                            var q = s.Split('|');
                            worldDic[int.Parse(q[0])] = q[1].Trim();
                        }
                    }
                }
                if (res.Name.ToLower() == $"FFXIV_ACT_Plugin.Resources.ZoneList_EN.txt".ToLower())
                {
                    using (var sr = new StreamReader((res as EmbeddedResource).GetResourceStream()))
                    {
                        var str = sr.ReadToEnd();
                        foreach (var s in str.Split('\n'))
                        {
                            var q = s.Split('|');
                            zoneDic[int.Parse(q[0])] = q[1];
                        }
                    }
                }
            }

            foreach (var z in zoneDic)
            {
                mapDic[z.Key] = z.Value;
            }

            #endregion

            #region FFXIV

            var realm = new ARealmReversed(gamePath, isCN ? SaintCoinach.Ex.Language.ChineseSimplified : Language.English);
            var items = realm.GameData.GetSheet<SaintCoinach.Xiv.Item>();
            foreach (var i in items)
            {
                var name = i.Name;
                var key = i.Key;
                if (!string.IsNullOrWhiteSpace(name))
                {
                    if (i.IsUntradable) key = -key;
                    itemDic[key] = name;
                }
            }

            #endregion

            allDic[DictionaryRetrieveKey.DAT_VER] = version;
            allDic[DictionaryRetrieveKey.WORLD] = worldDic;
            allDic[DictionaryRetrieveKey.MATERIA] = isCN ? materiaDicCN : materiaDicEN;
            allDic[DictionaryRetrieveKey.RET_LOC] = isCN ? retainerDicCN : retainerDicEN;
            allDic[DictionaryRetrieveKey.ITEM] = itemDic;
            allDic[DictionaryRetrieveKey.DYE] = isCN ? dyeDicCN : dyeDicEN;
            allDic[DictionaryRetrieveKey.MAP] = mapDic;
            allDic[DictionaryRetrieveKey.RET_LOCL_NICK] = isCN ? locNickCN : retainerDicEN;
            allDic[DictionaryRetrieveKey.MATERIA_NICK] = isCN ? materiaAbbrCN : materiaDicEN;
            allDic[DictionaryRetrieveKey.WORLD_NICK] = worldNameDic;
            allDic[DictionaryRetrieveKey.WORLD_SERVER] = worldServerDic;

            var save = Helper.GetMilFilePathRaw($"dictionary.pack");
            var ser = new Serializer<Dictionary<int, Dictionary<int, string>>>(save, "");
            ser.Save(allDic);
        }

